// 8. Write a Dart program that declares two numeric variables and performs addition, subtraction,
// multiplication, division, and modulo on them. Print each result.
void main() {
  int x = 25;
  int y = 4;
  int sum = x + y;
  print('Sum = $sum');
  int sub = x - y;
  print('subtraction = $sub');
  int mul = x * y;
  print('multiplication = $mul');
  double div = x / y;
  print('division = $div');
  int mod = x % y;
  print('modulo = $mod');
}
